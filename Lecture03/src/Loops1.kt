fun main() {
    println("1..3")
    for (i in 1..3) {
        println(i)
    }
    println()

    println("1 until 3")
    for (i in 1 until 3) {
        println(i)
    }
    println()

    println("6 downTo 0 step 2")
    for (i in 6 downTo 0 step 2) {
        println(i)
    }

    // https://kotlinlang.org/docs/control-flow.html#for-loops
}
